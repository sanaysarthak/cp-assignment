// Program in C to demonstrate the use of Arithmetical operators
#include <stdio.h>
int main()
{
    int a=20, b=10;
    printf("%d + %d = %d\n", a, b, a+b); // Addition
    printf("%d - %d = %d\n", a, b, a-b); // Subtraction
    printf("%d * %d = %d\n", a, b, a*b); // Multiplication
    printf("%d / %d = %d\n", a, b, a/b); // Division
    return 0;
}